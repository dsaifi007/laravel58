    {
        Validator::make($request->all(), [
            'role_name' => 'required|unique:roles|max:255|min:4',
            'role_description' => 'required',
        ])->validate();
        Role::create($request->all());
        return redirect('/add/role')->with('added','Role successfully added');
    }

